#include "push_swap.h"

int	ft_atoi(const char *str)
{
	int				i;
	int				pre;
	long long int	ans;

	i = 0;
	pre = 1;
	ans = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			pre = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		ans = ans * 10 + (str[i] - '0');
		i++;
	}
	if (ans < -2147483648 || ans > 2147483647)
		return (0);
	return (pre * ans);
}
